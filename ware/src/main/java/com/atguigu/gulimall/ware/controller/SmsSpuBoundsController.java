import com.atguigu.gulimall.ware.entity.SmsSpuBoundsEntity;
import com.atguigu.gulimall.ware.service.SmsSpuBoundsService;
import com.atguigu.common.utils.PageUtils;
import com.atguigu.common.utils.R;



