      <el-input v-model="dataForm.consumeAmount" placeholder="ГҖГӣВјГҶГҸГ»В·Г‘ВҪГ°В¶Г®"></el-input>
    </el-form-item>
    <el-form-item label="ГҖГӣВјГҶГ“Г…В»ГқВҪГ°В¶Г®" prop="couponAmount">
      <el-input v-model="dataForm.couponAmount" placeholder="ГҖГӣВјГҶГ“Г…В»ГқВҪГ°В¶Г®"></el-input>
    </el-form-item>
    <el-form-item label="В¶В©ВөВҘГҠГҪГҒВҝ" prop="orderCount">
      <el-input v-model="dataForm.orderCount" placeholder="В¶В©ВөВҘГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="Г“Г…В»ГқГҲВҜГҠГҪГҒВҝ" prop="couponCount">
      <el-input v-model="dataForm.couponCount" placeholder="Г“Г…В»ГқГҲВҜГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="ГҶГҖВјГӣГҠГҪ" prop="commentCount">
