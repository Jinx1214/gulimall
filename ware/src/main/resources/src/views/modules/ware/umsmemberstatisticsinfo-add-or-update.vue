    </el-form-item>
    <el-form-item label="ГҚГӢВ»ГөГҠГҪГҒВҝ" prop="returnOrderCount">
      <el-input v-model="dataForm.returnOrderCount" placeholder="ГҚГӢВ»ГөГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="ВөГҮГӮВјВҙГҺГҠГҪ" prop="loginCount">
      <el-input v-model="dataForm.loginCount" placeholder="ВөГҮГӮВјВҙГҺГҠГҪ"></el-input>
    </el-form-item>
    <el-form-item label="В№ГҳГ—ВўГҠГҪГҒВҝ" prop="attendCount">
      <el-input v-model="dataForm.attendCount" placeholder="В№ГҳГ—ВўГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="В·ГӣГӢВҝГҠГҪГҒВҝ" prop="fansCount">
      <el-input v-model="dataForm.fansCount" placeholder="В·ГӣГӢВҝГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГ•ВІГҳВөГ„ГүГҢГҶВ·ГҠГҪГҒВҝ" prop="collectProductCount">
      <el-input v-model="dataForm.collectProductCount" placeholder="ГҠГ•ВІГҳВөГ„ГүГҢГҶВ·ГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГ•ВІГҳВөГ„Г—ВЁГҢГўВ»Г®В¶ВҜГҠГҪГҒВҝ" prop="collectSubjectCount">
      <el-input v-model="dataForm.collectSubjectCount" placeholder="ГҠГ•ВІГҳВөГ„Г—ВЁГҢГўВ»Г®В¶ВҜГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГ•ВІГҳВөГ„ГҶГҖГӮГӣГҠГҪГҒВҝ" prop="collectCommentCount">
      <el-input v-model="dataForm.collectCommentCount" placeholder="ГҠГ•ВІГҳВөГ„ГҶГҖГӮГӣГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    <el-form-item label="Г‘Г»ГҮГ«ВөГ„Г…ГіГ“Г‘ГҠГҪГҒВҝ" prop="inviteFriendCount">
      <el-input v-model="dataForm.inviteFriendCount" placeholder="Г‘Г»ГҮГ«ВөГ„Г…ГіГ“Г‘ГҠГҪГҒВҝ"></el-input>
    </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">еҸ–ж¶Ҳ</el-button>
      <el-button type="primary" @click="dataFormSubmit()">зЎ®е®ҡ</el-button>
    </span>
  </el-dialog>
</template>

<script>
  export default {
    data () {
      return {
        visible: false,
        dataForm: {
          id: 0,
          memberId: '',
          consumeAmount: '',
          couponAmount: '',
          orderCount: '',
          couponCount: '',
          commentCount: '',
          returnOrderCount: '',
          loginCount: '',
          attendCount: '',
          fansCount: '',
          collectProductCount: '',
          collectSubjectCount: '',
          collectCommentCount: '',
          inviteFriendCount: ''
        },
        dataRule: {
          memberId: [
            { required: true, message: 'В»ГЎГ”ВұidдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          consumeAmount: [
            { required: true, message: 'ГҖГӣВјГҶГҸГ»В·Г‘ВҪГ°В¶Г®дёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          couponAmount: [
            { required: true, message: 'ГҖГӣВјГҶГ“Г…В»ГқВҪГ°В¶Г®дёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          orderCount: [
            { required: true, message: 'В¶В©ВөВҘГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          couponCount: [
            { required: true, message: 'Г“Г…В»ГқГҲВҜГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          commentCount: [
            { required: true, message: 'ГҶГҖВјГӣГҠГҪдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          returnOrderCount: [
            { required: true, message: 'ГҚГӢВ»ГөГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          loginCount: [
            { required: true, message: 'ВөГҮГӮВјВҙГҺГҠГҪдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          attendCount: [
            { required: true, message: 'В№ГҳГ—ВўГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          fansCount: [
            { required: true, message: 'В·ГӣГӢВҝГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          collectProductCount: [
            { required: true, message: 'ГҠГ•ВІГҳВөГ„ГүГҢГҶВ·ГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          collectSubjectCount: [
            { required: true, message: 'ГҠГ•ВІГҳВөГ„Г—ВЁГҢГўВ»Г®В¶ВҜГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          collectCommentCount: [
            { required: true, message: 'ГҠГ•ВІГҳВөГ„ГҶГҖГӮГӣГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          inviteFriendCount: [
            { required: true, message: 'Г‘Г»ГҮГ«ВөГ„Г…ГіГ“Г‘ГҠГҪГҒВҝдёҚиғҪдёәз©ә', trigger: 'blur' }
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.id = id || 0
        this.visible = true
        this.$nextTick(() => {
          this.$refs['dataForm'].resetFields()
          if (this.dataForm.id) {
            this.$http({
              url: this.$http.adornUrl(`/ware/umsmemberstatisticsinfo/info/${this.dataForm.id}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.memberId = data.umsMemberStatisticsInfo.memberId
                this.dataForm.consumeAmount = data.umsMemberStatisticsInfo.consumeAmount
                this.dataForm.couponAmount = data.umsMemberStatisticsInfo.couponAmount
                this.dataForm.orderCount = data.umsMemberStatisticsInfo.orderCount
                this.dataForm.couponCount = data.umsMemberStatisticsInfo.couponCount
                this.dataForm.commentCount = data.umsMemberStatisticsInfo.commentCount
                this.dataForm.returnOrderCount = data.umsMemberStatisticsInfo.returnOrderCount
                this.dataForm.loginCount = data.umsMemberStatisticsInfo.loginCount
                this.dataForm.attendCount = data.umsMemberStatisticsInfo.attendCount
                this.dataForm.fansCount = data.umsMemberStatisticsInfo.fansCount
                this.dataForm.collectProductCount = data.umsMemberStatisticsInfo.collectProductCount
                this.dataForm.collectSubjectCount = data.umsMemberStatisticsInfo.collectSubjectCount
                this.dataForm.collectCommentCount = data.umsMemberStatisticsInfo.collectCommentCount
                this.dataForm.inviteFriendCount = data.umsMemberStatisticsInfo.inviteFriendCount
              }
            })
          }
        })
      },
      // иЎЁеҚ•жҸҗдәӨ
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/ware/umsmemberstatisticsinfo/${!this.dataForm.id ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'id': this.dataForm.id || undefined,
                'memberId': this.dataForm.memberId,
                'consumeAmount': this.dataForm.consumeAmount,
                'couponAmount': this.dataForm.couponAmount,
                'orderCount': this.dataForm.orderCount,
                'couponCount': this.dataForm.couponCount,
                'commentCount': this.dataForm.commentCount,
                'returnOrderCount': this.dataForm.returnOrderCount,
                'loginCount': this.dataForm.loginCount,
                'attendCount': this.dataForm.attendCount,
                'fansCount': this.dataForm.fansCount,
                'collectProductCount': this.dataForm.collectProductCount,
                'collectSubjectCount': this.dataForm.collectSubjectCount,
                'collectCommentCount': this.dataForm.collectCommentCount,
                'inviteFriendCount': this.dataForm.inviteFriendCount
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: 'ж“ҚдҪңжҲҗеҠҹ',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
              }
            })
          }
        })
      }
    }
  }
</script>
