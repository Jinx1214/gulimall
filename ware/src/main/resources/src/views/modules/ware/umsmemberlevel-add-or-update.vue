<template>
  <el-dialog
    :title="!dataForm.id ? 'ж–°еўһ' : 'дҝ®ж”№'"
    :close-on-click-modal="false"
    :visible.sync="visible">
    <el-form :model="dataForm" :rules="dataRule" ref="dataForm" @keyup.enter.native="dataFormSubmit()" label-width="80px">
    <el-form-item label="ВөГҲВјВ¶ГғГ»ВіГҶ" prop="name">
      <el-input v-model="dataForm.name" placeholder="ВөГҲВјВ¶ГғГ»ВіГҶ"></el-input>
    </el-form-item>
    <el-form-item label="ВөГҲВјВ¶ГҗГЁГ’ВӘВөГ„ВіГүВіВӨГ–Вө" prop="growthPoint">
      <el-input v-model="dataForm.growthPoint" placeholder="ВөГҲВјВ¶ГҗГЁГ’ВӘВөГ„ВіГүВіВӨГ–Вө"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГҮВ·ГұГҺВӘГ„В¬ГҲГҸВөГҲВјВ¶[0->ВІВ»ГҠГҮВЈВ»1->ГҠГҮ]" prop="defaultStatus">
      <el-input v-model="dataForm.defaultStatus" placeholder="ГҠГҮВ·ГұГҺВӘГ„В¬ГҲГҸВөГҲВјВ¶[0->ВІВ»ГҠГҮВЈВ»1->ГҠГҮ]"></el-input>
    </el-form-item>
    <el-form-item label="ГғГўГ”ГӢВ·Г‘ВұГӘГ—Вј" prop="freeFreightPoint">
      <el-input v-model="dataForm.freeFreightPoint" placeholder="ГғГўГ”ГӢВ·Г‘ВұГӘГ—Вј"></el-input>
    </el-form-item>
    <el-form-item label="ГғВҝВҙГҺГҶГҖВјГӣВ»ГұГҲВЎВөГ„ВіГүВіВӨГ–Вө" prop="commentGrowthPoint">
      <el-input v-model="dataForm.commentGrowthPoint" placeholder="ГғВҝВҙГҺГҶГҖВјГӣВ»ГұГҲВЎВөГ„ВіГүВіВӨГ–Вө"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГҮВ·ГұГ“ГҗГғГўГ“ГҠГҢГҳГҲВЁ" prop="priviledgeFreeFreight">
      <el-input v-model="dataForm.priviledgeFreeFreight" placeholder="ГҠГҮВ·ГұГ“ГҗГғГўГ“ГҠГҢГҳГҲВЁ"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГҮВ·ГұГ“ГҗВ»ГЎГ”ВұВјГӣВёГұГҢГҳГҲВЁ" prop="priviledgeMemberPrice">
      <el-input v-model="dataForm.priviledgeMemberPrice" placeholder="ГҠГҮВ·ГұГ“ГҗВ»ГЎГ”ВұВјГӣВёГұГҢГҳГҲВЁ"></el-input>
    </el-form-item>
    <el-form-item label="ГҠГҮВ·ГұГ“ГҗГүГәГҲГ•ГҢГҳГҲВЁ" prop="priviledgeBirthday">
      <el-input v-model="dataForm.priviledgeBirthday" placeholder="ГҠГҮВ·ГұГ“ГҗГүГәГҲГ•ГҢГҳГҲВЁ"></el-input>
    </el-form-item>
    <el-form-item label="ВұВёГ—Вў" prop="note">
      <el-input v-model="dataForm.note" placeholder="ВұВёГ—Вў"></el-input>
    </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">еҸ–ж¶Ҳ</el-button>
      <el-button type="primary" @click="dataFormSubmit()">зЎ®е®ҡ</el-button>
    </span>
  </el-dialog>
</template>

<script>
  export default {
    data () {
      return {
        visible: false,
        dataForm: {
          id: 0,
          name: '',
          growthPoint: '',
          defaultStatus: '',
          freeFreightPoint: '',
          commentGrowthPoint: '',
          priviledgeFreeFreight: '',
          priviledgeMemberPrice: '',
          priviledgeBirthday: '',
          note: ''
        },
        dataRule: {
          name: [
            { required: true, message: 'ВөГҲВјВ¶ГғГ»ВіГҶдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          growthPoint: [
            { required: true, message: 'ВөГҲВјВ¶ГҗГЁГ’ВӘВөГ„ВіГүВіВӨГ–ВөдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          defaultStatus: [
            { required: true, message: 'ГҠГҮВ·ГұГҺВӘГ„В¬ГҲГҸВөГҲВјВ¶[0->ВІВ»ГҠГҮВЈВ»1->ГҠГҮ]дёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          freeFreightPoint: [
            { required: true, message: 'ГғГўГ”ГӢВ·Г‘ВұГӘГ—ВјдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          commentGrowthPoint: [
            { required: true, message: 'ГғВҝВҙГҺГҶГҖВјГӣВ»ГұГҲВЎВөГ„ВіГүВіВӨГ–ВөдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          priviledgeFreeFreight: [
            { required: true, message: 'ГҠГҮВ·ГұГ“ГҗГғГўГ“ГҠГҢГҳГҲВЁдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          priviledgeMemberPrice: [
            { required: true, message: 'ГҠГҮВ·ГұГ“ГҗВ»ГЎГ”ВұВјГӣВёГұГҢГҳГҲВЁдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          priviledgeBirthday: [
            { required: true, message: 'ГҠГҮВ·ГұГ“ГҗГүГәГҲГ•ГҢГҳГҲВЁдёҚиғҪдёәз©ә', trigger: 'blur' }
          ],
          note: [
            { required: true, message: 'ВұВёГ—ВўдёҚиғҪдёәз©ә', trigger: 'blur' }
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.id = id || 0
        this.visible = true
        this.$nextTick(() => {
          this.$refs['dataForm'].resetFields()
          if (this.dataForm.id) {
            this.$http({
              url: this.$http.adornUrl(`/ware/umsmemberlevel/info/${this.dataForm.id}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.name = data.umsMemberLevel.name
                this.dataForm.growthPoint = data.umsMemberLevel.growthPoint
                this.dataForm.defaultStatus = data.umsMemberLevel.defaultStatus
                this.dataForm.freeFreightPoint = data.umsMemberLevel.freeFreightPoint
                this.dataForm.commentGrowthPoint = data.umsMemberLevel.commentGrowthPoint
                this.dataForm.priviledgeFreeFreight = data.umsMemberLevel.priviledgeFreeFreight
                this.dataForm.priviledgeMemberPrice = data.umsMemberLevel.priviledgeMemberPrice
                this.dataForm.priviledgeBirthday = data.umsMemberLevel.priviledgeBirthday
                this.dataForm.note = data.umsMemberLevel.note
              }
            })
          }
        })
      },
      // иЎЁеҚ•жҸҗдәӨ
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/ware/umsmemberlevel/${!this.dataForm.id ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'id': this.dataForm.id || undefined,
                'name': this.dataForm.name,
                'growthPoint': this.dataForm.growthPoint,
                'defaultStatus': this.dataForm.defaultStatus,
                'freeFreightPoint': this.dataForm.freeFreightPoint,
                'commentGrowthPoint': this.dataForm.commentGrowthPoint,
                'priviledgeFreeFreight': this.dataForm.priviledgeFreeFreight,
                'priviledgeMemberPrice': this.dataForm.priviledgeMemberPrice,
                'priviledgeBirthday': this.dataForm.priviledgeBirthday,
                'note': this.dataForm.note
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: 'ж“ҚдҪңжҲҗеҠҹ',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
              }
            })
          }
        })
      }
    }
  }
</script>
